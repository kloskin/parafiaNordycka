<h3>1. NIEDZIELA ADWENTU (27.11.2022)</h3><br/><br/>
<p class="tekst">
1.Dzisiaj tj. w niedzielę 27 listopada:
 1)Rozpoczyna się Adwent i nowy rok liturgiczny, który w polskim Kościele będzie przeżywany pod hasłem: „Wierzę w Kościół Chrystusowy”;
2) Wszystkich Parafian zapraszamy na rekolekcje adwentowe, które będą trwały od dzisiaj do środy tj, 30 listopada. Rekolekcje poprowadzi Ojciec Wojciech Trzciński OMI z Poznania. Nauki rekolekcyjne będą głoszone dzisiaj podczas każdej Mszy św., a w poniedziałek, wtorek i środę podczas Mszy św. o godz. 18.00. Ofiary złożone na tacę w środę będą wyrazem naszej wdzięczności wobec ojca rekolekcjonisty. Zachęcamy do takiego zaplanowania swoich zajęć, by znaleźć czas na uczestniczenie w tym duchowym przygotowaniu do Świąt Bożego Narodzenia,
3) Przed kościołem zbiórka ofiar na rzecz Arcybiskupiego Seminarium Duchownego w Poznaniu.
<br/><br/>
2.W okresie Adwentu serdecznie zapraszam wszystkich Parafian na roraty, które we wtorki i czwartki będą rano o godz. 6.00 (po Mszach św. o godz. 6:00, zapraszamy na śniadanie do Arki), a w poniedziałki, środy i piątki wieczorem o godz. 18.00, a w soboty o godz. 8.00. Dzieci prosimy o przynoszenie lampionów i serduszek z dobrymi uczynkami. Codziennie będą losowane dzieci, które zabiorą do swoich domów, na całą dobę, figurki Matki Bożej i św. Józefa.
<br/><br/>
3.W środę spacer miłosierdzia od 20:00 do 21:00.
<br/><br/>
4.W tym tygodniu przypada I czwartek, piątek i sobota miesiąca.
<br/><br/>
5. W czwartek: 1) Adoracja Najświętszego sakramentu od 18.30 do 21.00, 2) Zbiórka ministrantów starszych o godz. 19.00.
<br/><br/>
6.W piątek: 1) Spowiedź dla dzieci od godz. 16.00 (Komunia św. o godz. 16.30), a dla młodzieży i dorosłych od godz. 17.15, 2) Spotkanie Duszpasterstwa Młodzieży w piątek o 19:00 w Arce.
<br/><br/>
7.W sobotę: 1) Parafialna pielgrzymka na Jasną Górę wyruszy o godz. 6.00. Zbiórka na parkingu przy kościele. Jest jeszcze kilka wolnych miejsc. (Koszt pielgrzymki 140 zł), 2) Nabożeństwo Pierwszych Sobót Miesiąca: o godz. 7.30 wystawienie Najśw. Sakramentu i różaniec, o godz. 8.00 Msza św. roratnia, 3) Zbiórka młodszych ministrantów i kandydatów na ministrantów o godz. 9:30.
<br/><br/>
8.Przedświąteczne odwiedziny chorych: 1) Ks. Proboszcz w sobotę tj. 10 grudnia, początek o godz. 8.30, 2) Ks. Damian w sobotę tj. 17 grudnia, początek o godz. 8.30.
<br/><br/>
9.W następną niedzielę tj. 4 grudnia: 1) Przypada XXIII Dzień modlitwy i pomocy materialnej Kościołowi na Wschodzie. Przed kościołem odbędzie się zbiórka do puszek na Fundusz Pomocy Kościołowi na Wschodzie, 2) Spotkanie z rodzicami dzieci przygotowujących się do Pierwszej Komunii świętej po Mszy św. o godz. 11.00; 3) Rozpocznie się w naszych rodzinach peregrynacja obrazu Matki Bożej Częstochowskiej, który znajduje się przy ołtarzu i zostanie poświęcony w Częstochowie podczas pielgrzymki parafialnej w sobotę tj. 3 grudnia. Rodziny, które chcą przyjąć obraz Matki Bożej w swoich domach proszone są w wpisanie się na listę znajdującą się na skarbonie w tylnej części kościoła (Obraz można przyjąć na 1 lub 2 dni).
<br/><br/>
10. Od II Niedzieli Adwentu tj. 4 grudnia parafialny zespół Caritas będzie rozprowadzał świece caritas na stół wigilijny oraz opłatki wigilijne. Pragniemy zaznaczyć, że ofiary składane przy okazji nabycia opłatków są przeznaczane na zakup komunikantów i hostii używanych podczas Mszy św. w naszym kościele.
<br/><br/>
11.Biuro parafialne w tym tygodniu będzie czynne: w poniedziałek i czwartek od 19.00 do 20.00, w środę od 9.00 do10.00.
<br/><br/>
12.Poświęcenie medalików dzieciom przygotowującym się do Pierwszej Komunii św. w niedzielę tj. 11 grudnia na Mszy św. o godz. 11.00.
<br/><br/>
13.Polecamy nowy numer „Przewodnika Katolickiego” (9,50 zł).
<br/><br/>
14.W minionym tygodniu zmarł nasz parafianin śp. Paweł Krygier, mieszkał na ul. Kierskiej (Msza św. pogrzebowa w par. pw. Dobrego Pasterza w Poznaniu o godz. 11.00, a następnie pogrzeb). Polećmy go miłosierdziu Bożemu…<br/><br/>
</p>
